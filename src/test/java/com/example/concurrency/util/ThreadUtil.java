package com.example.concurrency.util;

public class ThreadUtil {

    public static void startAndJoinThread(Thread... threads) {
        for (Thread thread : threads) {
            thread.start();
        }

        for (Thread thread : threads) {
            try {
                thread.join();
            } catch (InterruptedException e) {
                throw new RuntimeException(e);
            }
        }
    }
}
